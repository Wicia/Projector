    private void showLayer(LayerName name){
        layers.stream().forEach((JLayeredPane pane) -> {
            if(pane.getName().equals(name.toString())){
                pane.setVisible(Boolean.TRUE);
            }
            else{
                pane.setVisible(Boolean.FALSE);
            }
        });
    }
    
